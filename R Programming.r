#VARIABLES AND CONSTANT

#Boolean variables
a=TRUE
print(a)
print(class(a))

#integer variables
A=14L
print(A)
print(class(A))

#floating point variables
x=13.4
print(x)
print(class(x))

#character variables
alphabhet="A"
print(alphabhet)
print(class(alphabhet))

#string variables
message="Welcome to India"
print(message)
print((class(message)))




#changing value of variable
message="HELLO WORLD"
print(message)
message<-"VinZie"
print(message)

#integer constant
x<-15L
print(typeof(x))
print(class(x))

